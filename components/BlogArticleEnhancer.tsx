function getLang(): Lang {
  const first = window.location.pathname.split("/").filter(Boolean)[0];
  if (first === "en" || first === "ru" || first === "ua") return first;
  return "ro";
}

function isBlogPostPage() {
  const parts = window.location.pathname.split("/").filter(Boolean);
  const blogIndex = parts.indexOf("blog");
  return blogIndex >= 0 && parts.length > blogIndex + 1;
}

function normalize(value: string) {
  return value.replace(/\s+/g, " ").trim();
}

function safeFileName(value: string) {
  return value
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9а-яёіїєґ\u0400-\u04ff]+/gi, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 90) || "revimed-articol";
}

function looksLikeHeading(text: string) {
  const t = normalize(text);

  if (t.length < 8 || t.length > 140) return false;

  return (
    /^\d+[\.\)]\s+/.test(t) ||
    /^✓\s*\d+[\.\)]\s+/.test(t) ||
    t.endsWith("?") ||
    /^De ce /i.test(t) ||
    /^Ce /i.test(t) ||
    /^Când /i.test(t) ||
    /^Cum /i.test(t) ||
    /^Why /i.test(t) ||
    /^What /i.test(t) ||
    /^When /i.test(t) ||
    /^How /i.test(t) ||
    /^Почему /i.test(t) ||
    /^Что /i.test(t) ||
    /^Когда /i.test(t) ||
    /^Как /i.test(t) ||
    /^Чому /i.test(t) ||
    /^Що /i.test(t) ||
    /^Коли /i.test(t) ||
    /^Як /i.test(t)
  );
}

function looksLikePhone(text: string) {
  return /(\+373|022\s*60\s*50\s*60|79\s*422\s*908|079\s*422\s*908)/.test(text);
}

function looksLikeDisclaimer(text: string) {
  const t = text.toLowerCase();

  return (
    t.includes("articol") && t.includes("consulta") ||
    t.includes("revimed plus+ nu este centru oncologic") ||
    t.includes("nu înlocuiește") ||
    t.includes("nu inlocuieste") ||
    t.includes("онколог") ||
    t.includes("інформаційний") ||
    t.includes("informativ")
  );
}

function looksLikeImportant(text: string) {
  const t = text.toLowerCase();
  return (
    t.startsWith("important") ||
    t.startsWith("важно") ||
    t.startsWith("важливо") ||
    t.startsWith("un lucru important")
  );
}

function enhanceArticle() {
  if (!isBlogPostPage()) return;

  const content = document.querySelector<HTMLElement>(".blogReadableContent");
  const article = document.querySelector<HTMLElement>(".blogPostArticle, .blogArticleCard, article");

  if (!content || !article) return;
  if (content.dataset.enhanced === "true") return;

  content.dataset.enhanced = "true";

  const children = Array.from(content.children) as HTMLElement[];

  children.forEach((el, index) => {
    const text = normalize(el.textContent || "");
    if (!text) return;

    el.classList.add("blogPrettyParagraph");

    // First paragraph = intro box.
    if (index === 0) {
      el.classList.add("blogIntroBox");
      return;
    }

    if (looksLikeHeading(text)) {
      el.classList.remove("blogPrettyParagraph");
      el.classList.add("blogAutoHeading");
      if (!text.startsWith("✓")) {
        el.textContent = `✓ ${text}`;
      }
      return;
    }

    if (looksLikeImportant(text)) {
      el.classList.add("blogImportantBox");
      return;
    }

    if (looksLikeDisclaimer(text)) {
      el.classList.add("blogDisclaimerLine");
      return;
    }

    if (looksLikePhone(text)) {
      el.classList.add("blogPhoneLine");
      return;
    }

    // Highlight key medical words inside paragraph.
    const html = el.innerHTML;
    if (!el.dataset.marked) {
      el.innerHTML = html
        .replace(/\b(prevenția|prevenție|screeningul|screening|analize|oncomarker|markerii tumorali|fosfataza alcalină|medic|oncolog|simptome|diagnostic)\b/gi, '<mark class="blogAutoMark">$1</mark>')
        .replace(/\b(prevention|screening|blood tests|tumor markers|alkaline phosphatase|doctor|oncologist|symptoms|diagnosis)\b/gi, '<mark class="blogAutoMark">$1</mark>')
        .replace(/\b(профилактика|скрининг|анализы|онкомаркеры|щелочная фосфатаза|врач|онколог|симптомы|диагноз)\b/gi, '<mark class="blogAutoMark">$1</mark>')
        .replace(/\b(профілактика|скринінг|аналізи|онкомаркери|лужна фосфатаза|лікар|онколог|симптоми|діагноз)\b/gi, '<mark class="blogAutoMark">$1</mark>');
      el.dataset.marked = "true";
    }
  });

  addDownloadBox(article);
}

