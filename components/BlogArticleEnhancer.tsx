function downloadDocument() {
  const doc = buildDocumentHtml();
  if (!doc) return;

  const blob = new Blob(["\ufeff", doc.html], { type: "text/html;charset=utf-8" });
  const url = URL.createObjectURL(blob);

  const a = document.createElement("a");
  a.href = url;
  a.download = doc.filename;
  document.body.appendChild(a);
  a.click();
  a.remove();

  window.setTimeout(() => URL.revokeObjectURL(url), 800);
}

function addDownloadBox(article: HTMLElement) {
  if (article.querySelector(".blogExportBox")) return;

  const lang = getLang();
  const label = labels[lang];

  const box = document.createElement("div");
  box.className = "blogExportBox";
  box.innerHTML = `
    <div class="blogExportText">
      <strong>${label.saveTitle}</strong>
      <span>${label.saveDesc}</span>
    </div>
    <button type="button" class="blogExportBtn">${label.saveBtn}</button>
  `;

  box.querySelector("button")?.addEventListener("click", downloadDocument);
  article.appendChild(box);
}

export default function BlogArticleEnhancer() {
  useEffect(() => {
    enhanceArticle();

    const observer = new MutationObserver(() => enhanceArticle());
    observer.observe(document.body, { childList: true, subtree: true });

    return () => observer.disconnect();
  }, []);

  return null;
}
